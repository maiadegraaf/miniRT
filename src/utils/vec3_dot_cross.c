#include "minirt.h"

float	dot(t_vec4 u, t_vec4 v)
{
	return ((u[0] * v[0]) + (u[1] * v[1]) + (u[2] * v[2]));
}

t_vec4	cross(t_vec4 u, t_vec4 v)
{
	t_vec4	ret;

	ret[0] = (u[1] * v[2]) - (u[2] * u[1]);
	ret[1] = (u[2] * v[0]) - (u[0] * u[2]);
	ret[2] = (u[0] * v[1]) - (u[1] * u[0]);
	ret[3] = 0;
	return (ret);
}
